day_input = int(input())

if day_input == 1:
    print("Monday")
elif day_input == 2:
    print("Tuesday")
elif day_input == 3:
    print("Wednesday")
elif day_input == 4:
    print("Thursday")
elif day_input == 5:
    print("Friday")
elif day_input == 6:
    print("Saturday")
elif day_input == 7:
    print("Sunday")
else:
    print("Error")

